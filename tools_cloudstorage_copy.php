<p>Copy is net zo'n clouddienst als Dropbox en OneDrive. Niets nieuws onder de zon, of toch wel? Copy heeft de functies samenwerken en veiligheid als speerpunten. En, laten we maar meteen met de deur in huis vallen, ook de <strong>15 GB gratis opslagruimte</strong> maakt Copy bijzonder interessant.</p>                                
<p>Om het allemaal nog mooier te maken, als u <strong><a href="https://copy.com?r=ifSD0o" target="_blank">deze link gebruikt ontvangt u zelfs 20 GB gratis opslagruimte.</strong></a></p>
<p>De cloud opslagdienst Copy wordt snel populairder onder tabletgebruikers. De dienst is relatief nieuw, maar probeert in veel opzichten het bekende en immens populaire Dropbox voorbij te streven. Dat lijkt ze best goed te lukken, want na enkele dagen gebruik zien wij alleen maar voordelen en wint Copy het van Dropbox. We leggen uit waarom.</p>
<p>Natuurlijk, alles wat je in de map Copy bewaart, is automatisch beschikbaar op al je apparaten en computers waarop je Copy geïnstalleerd hebt. Maar daarnaast heeft Copy ‘Fair Storage’. Zowel Copy als Dropbox bieden je de kans om mappen en bestanden te delen. Stel dat je 5 GB aan bestanden deelt met een vriend, dan wordt er bij Dropbox bij beide gebruikers 5 GB opslagruimte als ‘bezet’ afgeboekt. Copy pakt dit anders aan. Met de zogenaamde ‘Fair Storage’ deelt Copy de gedeelde ruimte over het aantal gebruikers. In bovenstaand voorbeeld gebruiken beide gebruikers dus ieder 2,5 GB. Een stukje gunstiger voor de gebruiker dus.</p>
