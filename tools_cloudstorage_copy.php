<p>Copy heeft ook geen limieten voor de grote van bestanden. Je kunt dus grote bestanden of mappen delen zonder hoofdpijn. Met de Share functie kun je dus echt samenwerken aan bestanden. Alle gebruikers beschikken altijd over de meest recente versie van bestanden in de gedeelde map.</p>
<p>Met Send public link kun je een link versturen. Iedereen met deze link kan het bestand (of de map) downloaden. Hiermee gebruik je Copy als een vervanger van online bestandskoeriers als WeTransfer.</p>
<p>Naast natuurlijk de enorme gratis opslag ruimte (dropbox geeft bijvoorbeeld maar 2 GB), is Copy ook bijzonder veilig. Omdat Copy is het product van de veiligheid van het netwerk en back-up gigant Barracuda, worden uw bestanden opgeslagen op servers we controleren en beheren, niet met een 3rd party zoals de meeste cloud-opslagdiensten. Voor een verhoogde veiligheid, worden uw gegevens beschermd door meerdere lagen van encryptie, waaronder top-secret AES 256, zowel tijdens de overdracht en bij rust op Barracuda's enterprise-class cloud storage. En met geavanceerde functies zoals het veilig delen, bron validatie en verificatie van de identiteit, kunt u gemakkelijk ademen weten uw inhoud kan alleen gezien worden door de mensen die je wilt.</p>
<p>Gebouwd op dezelfde Cloud als Barracuda's bekroonde IT-oplossingen is Copy ook voor bedrijven een goede optie. <strong>U ontvangt namelijk de eerste 5 gebruikers gratis!!!</strong>. Voor kleine bedrijven is dit dus helemaal ideaal. En met enterprise-level security, eigen gegevens controle, veilig delen, en zo veel opslagruimte en zoveel gebruikers als je nodig hebt is Copy de CloudStorage oplossing die je wilt. Bovendien is Copy dus ontworpen om u de tools en ondersteuning te geven die u nodig hebt tegen een prijs die de concurrentie ruim verslaat.</p>
<p>Als laatste, en dan moet u eigenlijk Copy zelf maar eens gaan uitproberen, biedt Copy naast de opties om bestanden of mappen te delen, nog een andere interessante optie: <strong>bestandsgeschiedenis.</strong> Copy toont de meest recente versies van een bestand. Maar je kunt zien wie er precies aan een bestand heeft gewerkt. Dit is handig wanneer je samenwerkt aan een project, maar ook wanneer je een bestand alleen zelf gebruikt. Dankzij deze optie behoort het per ongeluk overschrijven van een document tot de verleden tijd. Je kunt namelijk oudere versies bekijken en herstellen via in Copy.</p>
								
